// Variáveis
int numero1 = 5;
int numero2 = 8;

// Maior
Console.WriteLine(numero1 > numero2);

// Maior ou igual
Console.WriteLine(numero1 >= numero2);

// Menor
Console.WriteLine(numero1 < numero2);

// Menor ou igual
Console.WriteLine(numero1 <= numero2);

// Igual
Console.WriteLine(numero1 == numero2);

// Diferente
Console.WriteLine(numero1 != numero2);
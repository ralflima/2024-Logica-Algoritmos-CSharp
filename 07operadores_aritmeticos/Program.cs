// Variáveis
int numero1 = 30;
int numero2 = 3;

// Divisão
Console.WriteLine(numero1 / numero2);

// Multiplicação
Console.WriteLine(numero1 * numero2);

// Subtração
Console.WriteLine(numero1 - numero2);

// Soma
Console.WriteLine(numero1 + numero2);

// Módulo (resto da divisão)
Console.WriteLine(numero1 % numero2);

// Incrementador
numero1++;
Console.WriteLine(numero1);

// Decrementador
numero2--;
Console.WriteLine(numero2);
